export function somar(num1, num2) {
    num1 + num2;
}

export function subtrair(num1, num2) {
    num1 - num2;
}

export function multiplicar(num1, num2) {
    num1 * num2;
}

export function dividir(num1, num2) {
    num1 / num2;
}

export function calculadora() {
    somar();
    subtrair();
    multiplicar();
    dividir();
}